package ex_05_TypeCasting;

public class task001 {
    public static void main(String[] args) {
//        print the table of 9 or 5 using the system.out.printf
        int num = 9;
        // 9 * 1 = 9
//         9 * 2 = 18
        // int -> %d
        System.out.printf("%d X 1 = %d",num, num*1);
        System.out.println();
        System.out.printf("%d X 2 = %d", num, num * 2).println();
        System.out.println("........");
        System.out.printf("%d X 10 = %d",num, num*10);
        System.out.println();
    }
}
