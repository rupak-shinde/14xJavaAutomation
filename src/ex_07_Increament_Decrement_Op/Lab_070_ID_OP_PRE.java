package ex_07_Increament_Decrement_Op;
public class Lab_070_ID_OP_PRE {
    public static void main(String[] args) {




        int a = 10 ;
        int b = ++a ;
        System.out.println(a);
        System.out.println(b);

        // ++a -> a = a+1, Increment by 1

        // Exp and Result Table
        // Line No | a | Result b
        // 8 | 10 | NA
        // 9 | 11 |  11
        // 11 | 11  - Print
    }
}
