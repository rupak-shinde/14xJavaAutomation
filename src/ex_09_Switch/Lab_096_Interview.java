package ex_09_Switch;

public class Lab_096_Interview {
    public static void main(String[] args) {
        char code = 'C';
        switch (code)
        {
            default:
                System.out.println("Hellowww!");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
